palavras_a= {'amor','amora','anjo','arca'} #conjunto de palavras que começã com a letra a
palavra_fa= {'janela','praia','bola','amora'}#conjunto de palavras que termina com a letra a
#fazendo o uso da operação intersection para encontrar palavras que pertece ao mesmo conjunto.
conjunto=palavras_a.intersection(palavra_fa)
#mostrando na tela as palavras que pertence ao mesmo conjunto.
print(conjunto)                             
